public class ShortHandArithmeticOperator4 {

	public static void main(String[] args){
		
		int sum = 10;
		sum  += 100; // sum = sum + 100;
		System.out.println(sum);//110
		

		int a = 10;
		a  -= 5; //a = a - 5
		System.out.println(a); //5


		int b = 10;
		b  *= 5; //b = b * 5
		System.out.println(b); //50



		int c = 10;
		c  /= 5; //c = c / 5
		System.out.println(c); //2



		int d = 21;
		d  %= 5; //d = d % 5
		System.out.println(d); //1

	}

}