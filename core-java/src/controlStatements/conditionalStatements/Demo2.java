package controlStatements.conditionalStatements;

public class Demo2 {
    public static void main(String[] args) {
        int number = 98;
        if (number % 2 == 0) {
            System.out.println("Its an even number");
        }
        if (number % 2 != 0) {
            System.out.println("Its an odd number");
        }
    }
}
